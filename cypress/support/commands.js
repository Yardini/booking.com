Cypress.Commands.add('customizeCommand', (ele, compText) => {
    console.log(ele, compText)
    ele.then((itemTag) => {
        var d = itemTag.text()
        console.log(d, "elements are printing")
        expect(d).to.eq(compText)
    })
})
